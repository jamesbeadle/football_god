<p class="my-2">
  Eventually the total supply of $FOOTBALL tokens will reduce to a minimum limit
  set by the DAO, that limit initially set at at 25%. When this minimum amount
  of $FOOTBALL tokens in circulation reaches this point a cashier within the
  FootballGod platform becomes active, allowing users to swap $ICP for newly
  minted $FOOTBALL tokens. When the total supply reaches its initial starting
  point, the cashier will close until the minimum limit is reached. This process
  allows the DAO to continually make revenue, frequently allowing new potential
  investment thus increasing the platforms decentralisation.
</p>

