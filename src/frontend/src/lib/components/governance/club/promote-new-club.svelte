<script lang="ts">
  import FormComponent from "$lib/components/shared/form-component.svelte";
  import Modal from "$lib/components/shared/modal.svelte";
  import { governanceStore } from "$lib/stores/governance-store";
  import { isError } from "$lib/utils/helpers";
  import type { ClubDTO, FootballLeagueDTO, LeagueId, PromoteClubDTO, ShirtType } from "../../../../../../declarations/data_canister/data_canister.did";
  import GovernanceModal from "../governance-modal.svelte";

  export let visible: boolean;
  export let closeModal: () => void;
  export let selectedClub: ClubDTO;
  export let selectedLeagueId: LeagueId;
    
  let isLoading = false;
  let promotionLeagues: FootballLeagueDTO[] = []

  let newLeagueId: LeagueId = 0;

  $: isSubmitDisabled = false;

  async function confirmProposal() {
    isLoading = true;
      let dto: PromoteClubDTO = {
      clubId: selectedClub.id,
      toLeagueId: newLeagueId,
      leagueId: selectedLeagueId,
    };
    let result = await governanceStore.promoteClub(dto);
    if (isError(result)) {
      isLoading = false;
      console.error("Error submitting proposal");
      return;
    }
    isLoading = false;
    resetForm();
    closeModal();
  }

  function resetForm() {
    newLeagueId = 0;
  }

  function cancelModal() {
    resetForm();
    closeModal();
  }
</script>

<Modal showModal={visible} onClose={closeModal}>
  <GovernanceModal title={"Promote New Club"} {cancelModal} {confirmProposal} {isLoading} {isSubmitDisabled}>
    <FormComponent label="New league:">
      <select
        class="brand-dropdown"
        bind:value={newLeagueId}
      >
        <option value={0}>Select League</option>
        {#each promotionLeagues as league}
          <option value={league.id}>{league.name}</option>
        {/each}
      </select>
    </FormComponent>
  </GovernanceModal>
</Modal>