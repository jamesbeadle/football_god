<script lang="ts">
  import { onMount } from "svelte";
  import { userStore } from "$lib/stores/user-store";
  import UpdateUsernameModal from "$lib/components/profile/update-username-modal.svelte";
  import CopyIcon from "$lib/icons/CopyIcon.svelte";
  import { writable } from "svelte/store";
  import WithdrawFplModal from "./withdraw-fpl-modal.svelte";
    import { authStore } from "$lib/stores/auth-store";
    import FullScreenSpinner from "../shared/full-screen-spinner.svelte";
    import { SHUFTI_CLIENT_ID, SHUFTI_SECRET_KEY } from "$lib/environment/environment";
    import { kycStore } from "$lib/stores/kyc-store";
    import type { ProfileDTO } from "../../../../../declarations/backend/backend.did";
    import { toasts } from "$lib/stores/toasts-store";
   
  let isLoading = true;
  let loadingBalances = true;

  let fplBalance = 0n;
  let fplBalanceFormatted = "0.0000"; 
  let dots = writable('.');
  let dot_interval: ReturnType<typeof setInterval>;
  let principalId = "";
  let profile: ProfileDTO | null = null;

  let showUsernameModal = false;
  let showFPLModal = false;

  let loadingKYC = false;

  onMount(async () => {
    try {
      startDotAnimation();
      await userStore.sync();
      await authStore.sync();
      principalId = $authStore.identity?.getPrincipal().toText() ?? "";
      unsubscribeUserProfile = userStore.subscribe((value) => {
        if (!value) {
          return;
        }
        profile = value;
      });
      unsubscribeUserProfile();
      isLoading = false;
      await fetchBalances();
    } catch (error) {
      console.error("Error fetching profile detail:", error);
      isLoading = false;
    }
  });

  let unsubscribeUserProfile: () => void;

  function startDotAnimation(){
    let count = 1;
    dot_interval = setInterval(() => {
      count = (count % 3) + 1;
      dots.set('.'.repeat(count));
    }, 500);
  }
  
  function loadWithdrawFPLModal(){
    showFPLModal = true;
  };

  async function closeWithdrawFPLModal(){
    showFPLModal = false;
    startDotAnimation();
    await fetchBalances();
  };

  

  async function fetchBalances() {
    try {
      let fplBalance = await userStore.getFPLBalance();
      fplBalanceFormatted = (Number(fplBalance) / 100_000_000).toFixed(4);
    } catch (error) {
      console.error("Error fetching profile detail:", error);
    } finally {
      clearInterval(dot_interval);
      loadingBalances = false;
    }
  }

  function displayUsernameModal(): void {
    showUsernameModal = true;
  }

  async function closeUsernameModal() {
    await userStore.cacheProfile();
    showUsernameModal = false;
  }

  function cancelUsernameModal() {
    showUsernameModal = false;
  }

  async function copyTextAndShowToast(text: string) {
    try {
      await navigator.clipboard.writeText(text);
      toasts.addToast({
        type: "success",
        message: "Copied to clipboard.",
        duration: 2000,
      });
    } catch (err) {
      console.error("Failed to copy:", err);
    }
  }
   
  async function beginKYC() {
    try {
      loadingKYC = true;
      let reference = crypto.randomUUID();
      await kycStore.storeKYCReference(reference);
      const payload = {
        reference: `SP_REQUEST_${reference}`,
        callback_url: "https://us-central1-openfpl1.cloudfunctions.net/api/forwardKYCResponse",
        redirect_url: "https://footballgod.xyz/profile",
        country: "GB",
        language: "EN",
        verification_mode: "any",
        ttl: 60,
        document: {
          supported_types: ["id_card", "passport"],
          allow_offline: "1",
          allow_online: "1",
        },
        address: {
          full_address: "48 Clare Crescent, Leatherhead, Surrey, KT22 7QZ",
          address_fuzzy_match: "1",
          supported_types: ["utility_bill", "passport", "bank_statement"],
        },
      };

      const token = btoa(`${SHUFTI_CLIENT_ID}:${SHUFTI_SECRET_KEY}`);
      const response = await fetch("https://api.shuftipro.com/", {
        method: "POST",
        headers: {
          Accept: "application/json",
          "Content-Type": "application/json",
          Authorization: `Basic ${token}`,
        },
        body: JSON.stringify(payload),
      });

      const data = await response.json();
      if (data.event === "request.pending") {
        let verificationUrl = data.verification_url;
        createIframe(verificationUrl);
      } else {
        console.error("Unexpected response:", data);
      }
    } catch (error) {
      console.error("Error starting KYC:", error);
    } finally {
      loadingKYC = false;
    }
  }

  function createIframe(src: string) {
    const iframe = document.createElement("iframe");
    iframe.style.position = "fixed";
    iframe.id = "shuftipro-iframe";
    iframe.name = "shuftipro-iframe";
    iframe.allow = "camera";
    iframe.src = src;
    iframe.style.top = "0";
    iframe.style.left = "0";
    iframe.style.bottom = "0";
    iframe.style.right = "0";
    iframe.style.margin = "0";
    iframe.style.padding = "0";
    iframe.style.overflow = "hidden";
    iframe.style.border = "none";
    iframe.style.zIndex = "2147483647";
    iframe.width = "100%";
    iframe.height = "100%";
    iframe.dataset.removable = "true";

    document.body.appendChild(iframe);
  }

  function getKycStatus(profile: ProfileDTO | null): string {
    if (!profile) return 'Not Started';
    if (!profile.kycRef) return 'Not Started';
    if (!profile.kycSubmissionDate) return 'Incomplete';
    if (!profile.kycApprovalDate) return 'Pending';
    if (!profile.kycComplete) return 'Rejected';
    return 'Verified';
  }
</script>

{#if isLoading || loadingKYC}
  <FullScreenSpinner />
{:else}
  <UpdateUsernameModal
    newUsername={$userStore ? $userStore.username : ""}
    visible={showUsernameModal}
    closeModal={closeUsernameModal}
    cancelModal={cancelUsernameModal}
  />
  <WithdrawFplModal
    visible={showFPLModal}
    closeModal={closeWithdrawFPLModal}
    cancelModal={closeWithdrawFPLModal}
    fplBalance={fplBalance}
    fplBalanceFormatted={fplBalanceFormatted}
  />
  <div class="container mx-6 mt-4">
    <div class="p-4 mb-4 rounded-lg bg-BrandGray">
      <h2 class="mb-4 text-xl text-white">Wallet Status</h2>
      
      <div class="flex flex-col gap-4">
        <div class="flex items-center justify-between">
          <span class="text-gray-300">Terms Status:</span>
          <span class="p-1 px-2 text-sm rounded md:text-sm md:p-2 md:px-4 {profile?.termsAcceptedDate ? 'bg-BrandPositive/20 text-BrandPositive' : 'bg-BrandNegative/20 text-BrandNegative'}">
            {profile?.termsAcceptedDate ? 'Terms Agreed' : 'Terms Not Agreed'}
          </span>
        </div>
        
        <div class="flex items-center justify-between">
          <span class="text-gray-300">KYC Verification Status:</span>
          <span class="p-1 px-2 text-sm rounded md:text-sm md:p-2 md:px-4" 
            class:bg-yellow-500={getKycStatus(profile) === 'Pending'}
            class:text-yellow-500={getKycStatus(profile) === 'Pending'}
            class:bg-BrandPositive={getKycStatus(profile) === 'Verified'}
            class:text-BrandPositive={getKycStatus(profile) === 'Verified'}
            class:bg-BrandNegative={['Not Started', 'Incomplete', 'Rejected'].includes(getKycStatus(profile))}
            class:text-black={['Not Started', 'Incomplete', 'Rejected'].includes(getKycStatus(profile))}
          >
            {getKycStatus(profile)}
          </span>
        </div>
      </div>
    </div>
    <div class="flex flex-wrap">
      <div class="w-full mb-4 md:mb-0">
        <div class="mt-2 rounded-lg md:mt-1">
          <p class="mb-1 text-xs">Username:</p>
          <h2 class="mb-1 default-header md:mb-2">
            {profile == null || profile.username == "" ? "Not Set" : profile.username}
          </h2>
          <button
            class="p-1 px-2 text-sm rounded md:text-sm md:p-2 md:px-4 fg-button button-hover"
            on:click={displayUsernameModal}
          >
            Update
          </button>

          <p class="mt-4 mb-1 text-xs">Principal:</p>
          <div class="flex items-center">
            <button
              class="flex items-center text-left break-all text-xxs"
              on:click={() => copyTextAndShowToast(principalId)}
            >
              <span>{ principalId }</span>
              <CopyIcon className="w-7 xs:w-6 text-left" fill="#FFFFFF" />
            </button>
          </div>
        </div>
      </div>
    </div>
    <div class="flex flex-wrap">
      <div class="w-full mb-4">
        <div class="mt-4">
          <div class="grid grid-cols-1 gap-4 md:grid-cols-2">
            <div
              class="flex items-center p-4 border border-gray-700 rounded-lg shadow-md"
            >
              <img
                src="/FPLCoin.png"
                alt="FPL"
                class="w-12 h-12 md:h-9 md:w-9"
              />
              <div class="ml-4 md:ml-3">

                  {#if loadingBalances}
                    <div class="dot-animation min-w-[20px]">{$dots}</div>
                  {:else}
                  <p>
                    {fplBalanceFormatted} FPL
                  </p>
                  {/if}

                <div class="flex items-center text-xs">
                  <button
                    class="flex items-center text-left break-all"
                    on:click={() => copyTextAndShowToast(principalId)}
                  >
                    <span>{principalId}</span>
                    <CopyIcon className="w-7 xs:w-6 text-left" fill="#FFFFFF" />
                  </button>
                </div>


                {#if !loadingBalances}
                <div class="flex items-center mt-2 text-xs">
                  <button
                  class="p-1 px-2 text-sm rounded md:text-sm md:p-2 md:px-4 fg-button button-hover"
                    on:click={loadWithdrawFPLModal}
                  >
                    Withdraw
                  </button>
                </div>
                {/if}
                
                {#if profile}
                  {#if profile.kycComplete}
                    <p class="mt-2 text-sm">KYC Verification Verified</p>
                  {:else}
                    <div class="flex items-center mt-2">
                      <div class="flex items-center mt-2 text-xs">
                        <button
                        class="p-1 px-2 text-sm rounded md:text-sm md:p-2 md:px-4 fg-button button-hover"
                          on:click={beginKYC}
                        >
                          KYC Verification
                        </button>
                      </div>
                    </div>
                  {/if}
                {/if}
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  </div>
{/if}